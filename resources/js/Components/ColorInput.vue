<script setup>
import { onMounted, ref } from 'vue';

defineProps({
    modelValue: {
        type: String,
        required: true,
    },
});

defineEmits(['update:modelValue']);

const input = ref(null);

onMounted(() => {
    if (input.value.hasAttribute('autofocus')) {
        input.value.focus();
    }
});

defineExpose({ focus: () => input.value.focus() });
</script>

<template>
    <input
        class="border-gray-500 focus:border-indigo-500 focus:ring-indigo-600 rounded-md shadow-sm 
            border h-10 bg-white focus:ring-1 cursor-pointer"
        :value="modelValue"
        @input="$emit('update:modelValue', $event.target.value)"
        ref="input"
        type="color"
    />
</template>
